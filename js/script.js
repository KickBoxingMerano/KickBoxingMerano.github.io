(function ($) {
    var o = $('.owl-carousel');
    if (o.length > 0) {
        include('js/owl.carousel.min.js');
        $(document).ready(function () {
            o.owlCarousel({
                items: 1,
                smartSpeed: 350,
                loop: false,
                dots: false,
                dotsEach: 1,
                nav: false,
                autoplay: true,
                autoplayTimeout: 5000,
                autoplayHoverPause: true,
                animateIn: 'fadeIn',
                navClass: ['owl-prev fa fa-angle-left', 'owl-next fa fa-angle-right']
            });

            var d = $('.dot');

            if (d.length) {
                d.on('click', function () {
                    console.log($(this).index());
                    $('.dot').removeClass('active');
                    $(this).addClass('active');
                    o.trigger('to.owl.carousel', [$(this).parent().index(), 1, true]);
                });
            }
        });
    }
})(jQuery);